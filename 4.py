# Запрос расстояния у пользователя
расстояние = float(input("Введите расстояние (в метрах): "))

# Запрос времени у пользователя
время = float(input("Введите время (в секундах): "))

# Расчет скорости
скорость = расстояние / время

# Вывод результата
print(f"Скорость равна: {скорость} м/с")